  function atomicSteps(recipe){
    const names=(recipe.ingredients||[]).map(ingredientName).filter(Boolean);
    const source=(recipe.steps||[]).map(stripQuotes).join(' ')
      .replace(/(^|[.;]\s*)Подготовьте ([^:.;]+):\s*/giu,'$1$2 ')
      .replace(new RegExp(`(Когда|Если|Пока|Как только) ([^.!?]+?),\\s+(?=${commandModifier}(?:${imperative})${commandEnd})`,'giu'),'$1 $2 — ')
      .replace(/Не (выключая|прекращая) ([^,.!?]+),\s+/giu,'Не $1 $2 — ')
      .replace(/;\s*/g,'. ')
      .replace(commandAfterComma,'. ')
      .replace(commandInShortClause,'. ')
      .replace(commandAfterAnd,'. ')
      .replace(new RegExp(`:\\s+(?=${commandModifier}(?:${imperative})${commandEnd})`,'giu'),'. ');
    const protectedSource=source
      .replace(/ст\.\s*л\./giu,'ст__л__')
      .replace(/ч\.\s*л\./giu,'ч__л__')
      .replace(/шт\./giu,'шт__')
      .replace(/мин\./giu,'мин__');
    const steps=protectedSource.split(/(?<=[.!?])\s+/u)
      .map(normalizeSpace)
      .filter(Boolean)
      .map(step=>step.replace(/ст__л__/giu,'ст. л.').replace(/ч__л__/giu,'ч. л.').replace(/шт__/giu,'шт.').replace(/мин__/giu,'мин.'))
      .map(step=>capitalize(step.replace(/^[—–-]\s*/,'')))
      .map(step=>/[.!?]$/.test(step)?step:`${step}.`)
      .filter(step=>!/^Отмерьте продукты по списку:/iu.test(step))
      .filter(step=>!/^Отмерьте (?:основные|остальные) ингредиенты по списку:/iu.test(step))
      .filter(step=>!/^Держите их рядом с рабочей зоной\.?$/iu.test(step))
      .filter(step=>step.trim().split(/\s+/u).length>1 || /йте[.!?]$/iu.test(step));
    const ingredientsStep=`Отмерьте продукты по списку: ${names.join(', ')}.`;
    const result=[ingredientsStep,...steps];
    return result.filter((step,index,list)=>index===0 || step!==list[index-1]);
  }

