  function normalizeStep(step){
    return String(step||'').toLocaleLowerCase('ru-RU')
      .replace(/ё/g,'е')
      .replace(/[^а-яa-z0-9]+/giu,' ')
      .trim();
  }

  function dedupeSteps(steps){
    const seen=new Set();
    return (steps||[]).filter(step=>{
      if(/^Отмерьте остальные (?:основные )?(?:ингредиенты|компоненты) по списку:/iu.test(step)) return false;
      const key=normalizeStep(step);
      if(!key || seen.has(key)) return false;
      seen.add(key);
      return true;
    });
  }

  function apply(recipes){
    recipes.filter(recipe=>recipe.country==='Корея').forEach(recipe=>{
      const patch=exact[recipe.id];
      if(patch){
        recipe.ingredients=[...patch.ingredients];
        const names=recipe.ingredients.map(ingredientName).filter(Boolean);
        recipe.steps=[`Отмерьте продукты по списку: ${names.join(', ')}.`,...patch.steps];
      }
      recipe.steps=dedupeSteps(recipe.steps);
    });
  }

